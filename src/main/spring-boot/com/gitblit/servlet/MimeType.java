    /**
     * content-type,content-type
     * 
     * @param fileName
     * @return
     * @throws Exception
     */
    public static String getMimeByFileName(String fileName) throws Exception {
        try {
            String extenName = fileName.substring(fileName.lastIndexOf("."), fileName.length());
            return getMime(extenName);

        } catch (Exception e) {
            return getMime(null);
        }
    }

    public static String getExtByMime(String mime) throws Exception {
        try {
            for (Entry<String, String> entry : extMap.entrySet()) {
                if(entry.getValue().equalsIgnoreCase(mime)){
                    return entry.getKey();
                }
            }

        } catch (Exception e) {
           e.printStackTrace();
        }
        return null;
    }

    /**
     * content-type,content-typeTomcat8meta
     * 
     * @param keyName
     * @return
     * @throws Exception
     */
    public static String getMime(String keyName) {

        if (keyName == null)
            return "application/octet-stream";
        if (keyName.indexOf(".") != 0)
            keyName = "." + keyName;
        String mimeName = "";

        mimeName = extMap.get(keyName);
        if (mimeName == null) {
            mimeName = "application/octet-stream";
        }
        return mimeName;

    }
